void spin_right(void)			  // 前向右打转函数
{ 
	IN1=0;
	IN2=1; // 左电机正转
	IN3=0;
	IN4=1; // 右电机反转
}

void spin_left(void)			  // 前向左打转函数
{
  IN1=1;
	IN2=0; // 左电机反转
	IN3=1;
	IN4=0; // 右电机正转
}


void spin_back_right(void)			  // 后向右打转函数，左轮应比后轮快
{ 
  IN1=1;
	IN2=0; // 左电机反转
	IN3=1;
	IN4=0; // 右电机正转
}

void spin_back_left(void)			    // 后向左打转函数,右轮应比左轮快
{
	IN1=0;
	IN2=1; // 左电机正转 
	IN3=0;
	IN4=1; // 右电机反转	
}