#include"motor.h"

void forward(void)			    // 前进函数
{
	IN1=0;
	IN2=1; // 左电机的正转
	IN3=1;
	IN4=0; // 右电机的正转
}

void back(void)			        // 后退函数
{
	IN1=1;
	IN2=0; // 左电机的反转
	IN3=0;
	IN4=1; // 右电机的反转
}

void stop(void)			        // 停止函数
{    
	IN1=0;
	IN2=0; // 左电机不动
	IN3=0;
	IN4=0; // 右电机正转
}

